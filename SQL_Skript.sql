        BLUTGRUPPE
        FROM "PATIENT"
        JOIN "KRANKENHAUS" K ON PATIENT.KRANKENHAUS_ID = K.KRANKENHAUS_ID
        JOIN "PATIENTENRAUM" PR ON PATIENT.PATIENTENRAUM_ID = PR.PATIENTENRAUM_ID
        JOIN "STATION" S ON PR.STATIONS_ID = S.STATIONS_ID
        
